saldo = 500
print(f'Saldo Inicial de: {saldo}')
saldo += 10
print(f'Adicionado 10 ao saldo: {saldo}')
saldo -= 20
print(f'Subtraido 20 ao saldo: {saldo}')
saldo //= 2
print(f'Saldo dividido por 2 (inteiro): {saldo}')
saldo /= 20
print(f'Saldo dividido por 20 (float): {saldo}')
saldo *= 4
print(f'Saldo Multiplicado por 4: {saldo}')
saldo **= 2
print(f'Saldo elevado ao quadrado: {saldo}')
saldo %= 3
print(f'Resto (módulo) do Saldo dividido por 3: {saldo}')

