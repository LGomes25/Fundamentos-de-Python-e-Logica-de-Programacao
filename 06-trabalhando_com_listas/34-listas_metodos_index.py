# index - identifica primeira ocorrencia do item pesquisado
linguagens = ['python', 'js', 'c','java', 'csharp', 'python']

print(linguagens)
print(f'Qual indica da lista da ocorrencia de java: {linguagens.index("java")}')
print(f'Qual indica da lista da ocorrencia de python: {linguagens.index("python")}')