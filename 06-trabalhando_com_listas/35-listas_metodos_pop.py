# pop - remove itens da uma pilha - do ultimo para o primeiro, ou pode ser direcionada por indices
linguagens = ['python', 'js', 'c','java', 'csharp', 'python']

print(linguagens)
print(f'remoção 1: {linguagens.pop()}')
print(linguagens)
print(f'remoção 2: {linguagens.pop()}')
print(linguagens)
print(f'remoção 3: {linguagens.pop()}')
print(linguagens)
print(f'remoção direcionada, index [0]: {linguagens.pop(0)}')
print(linguagens)